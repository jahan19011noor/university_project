						<h2>Performance Evaluation:</h2>
						<p>The performance of a student will be evaluated in terms of Semester GPA and cumulative grade point average (CGPA) which is the grade average for the semesters under consideration. Candidates will be awarded a degree with honors if  their overall CGPA is 3.75 or above. Students will be considered to be making normal progress towards a degree if their CGPA for all .work attempted is 2.25 or better and if they are in good standing with the university.
						Students who fail to maintain this minimum rate of progress will not be considered making desirable progress. This can happen when one or more of the following conditions exist: <br />
						I. &nbsp &nbsp &nbsp &nbsp Semester GPA falls below 2.25, <br />
						II. &nbsp &nbsp &nbsp Cumulative GPA falls below 2.25, <br />
						III. &nbsp &nbsp Earned credits fall below 15 times the number of semesters studied. <br />
						All such students can make up deficiencies in GPA and credit requirements by completing courses in next semester(s) and making up backlog courses, if there be any.</p>
						<h2>Rules for Courses Offered in Short Semester</h2>
						<p>The courses offered during the short semester shall be decided according to the recommendations of departments on the basis of essential deficiencies to be made up by a group of students. Other students could be allowed to register in those courses subject to capacity constraints and satisfaction of prerequisites. Students will be allowed to register for a maximum of two courses during the short semester. <br />
						A fee for each credit hour to be registered will have to be borne by the students who enroll during short Semester.</p>
